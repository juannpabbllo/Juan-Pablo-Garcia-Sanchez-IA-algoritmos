# Probabilidad a priori: se define antes de observar datos.

# Ejemplo: un robot bombero cree que hay un 5% de probabilidad de incendio en cualquier casa
probabilidad_incendio = 0.05
probabilidad_no_incendio = 1 - probabilidad_incendio

print("Probabilidades a priori:")
print(f"P(Incendio) = {probabilidad_incendio}")
print(f"P(No Incendio) = {probabilidad_no_incendio}")
